char	*ft_strrchr(const char *st, int c)
{
	int		i;
	char	*str;

	str = (char*)st;
	i = 0;
	while (str[i] != '\0')
		i++;
	if (c == '\0')
		return (str + i);
	while (i > -1)
	{
		if (str[i] == c)
			return (str + i);
		i--;
	}
	return (0);
}
