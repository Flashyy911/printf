char	*ft_strchr(const char *st, int c)
{
	int		i;
	char	*str;

	str = (char*)st;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			return (str + i);
		i++;
	}
	if (c == '\0')
		return (str + i);
	return (0);
}
