#include "../../headers/ft_printf.h"

int	ft_isprintfstring(char c)
{
	if (c == '%' || c == 'x' || c ==
			'X' || c == 's' || c == 'u' || c == 'd'
			|| c == 'i')
		return (1);
	return (0);
}
